package processingManga;

public class MangaCoeff {
    public double season;
    public double coeffA;
    public double coeffB;
    public double dispersion;

    public MangaCoeff(double season, double coeffA, double coeffB, double dispersion) {
        this.season = season;
        this.coeffA = coeffA;
        this.coeffB = coeffB;
        this.dispersion = dispersion;
    }
}
